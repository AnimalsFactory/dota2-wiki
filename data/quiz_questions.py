def get_questions_by_category(category):
    """Возвращает список вопросов определённой категории."""
    return [q for q in QUESTIONS if q["category"] == category]

def normalize(text):
    """Нормализует строку для сравнения."""
    return text.strip().lower()

def is_answer_correct(user_answer, correct_answers):
    """Проверяет ответ пользователя комбинированным методом."""
    from difflib import get_close_matches
    user_norm = normalize(user_answer)
    # Приводим правильные ответы к нормализованному виду
    normalized_correct = [normalize(a) for a in correct_answers]
    # Точное совпадение
    if user_norm in normalized_correct:
        return True
    # Нечёткое совпадение (разрешаем 1-2 опечатки)
    for correct in correct_answers:
        # Проверяем, совпадает ли user_norm с правильным ответом с допустимой погрешностью
        if get_close_matches(user_norm, [normalize(correct)], cutoff=0.8):
            return True
    return False